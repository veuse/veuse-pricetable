<h1>Pricetables</h1>
<h4>Pricetable entries are called <b>price-items</b>, and are organized in categories called pricetables.</h4>

<h3>How to create a pricetable</h3>
<ol>
	<li>Go to <a href="edit-tags.php?taxonomy=pricetable&post_type=priceitem" target="blank">Pricetable &raquo; Pricetable.</a></li>
	<li>Under the heading "Add New Pricetable", enter a name for your pricetable and click on the button <b>Add New Pricetable</b></li>
</ol>