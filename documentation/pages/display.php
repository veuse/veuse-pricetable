<h1>Displaying your pricetable</h1>
		
<h4>There are two ways of showing your pricetable on your site. You can either use a shortcode, or insert via a widget ( if you have installed Page Builder plugin by SiteOrigin</h4>

<code>[veuse_pricetable pricetable=""]</code>

<h3>Attributes</h3>
<p><b>pricetable:</b> Enter name (slug) of the pricetable you want to insert.</p>
